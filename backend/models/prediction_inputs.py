"""Single source of truth for assembling a match's model inputs + context modifiers.

Both the live API route (api/routes/predictions.py) and the offline prediction logger
(data/prediction_logger.py) call `assemble()` so they score the IDENTICAL model. The
logger previously built a stripped-down model (matchday only, all 10 lambda modifiers
defaulted to 1.0), which meant the public accuracy/ROI track record was generated by a
materially different engine than the one shown in the UI.
"""
from __future__ import annotations

import asyncio

from sqlalchemy.orm import Session

from backend.db.models import Match, Team
from backend.models.group_predictor import TeamInput
from backend.models.venue_advantage import get_venue_bonuses
from backend.models.match_context import (
    altitude_lambda_bonus,
    rest_days_multipliers,
    dead_rubber_multipliers,
    travel_multipliers,
)
from backend.data.fetchers.results import get_recent_form
from backend.data.fetchers.squad_values import get_squad_quality_multipliers
from backend.data.fetchers.injuries import get_injury_multipliers
from backend.data.fetchers.head_to_head import get_h2h_multipliers
from backend.data.fetchers.weather import get_weather_multipliers
from backend.data.fetchers.lineups import get_lineup_multipliers
from backend.data.fetchers.squad_xg import get_squad_attack_multipliers
from backend.data.fetchers.team_xg import get_xg_form_multipliers
from backend.data.fetchers.squad_availability import get_squad_availability_multipliers
from backend.data.fetchers.set_pieces import get_set_piece_multipliers
from backend.data.fetchers.team_xg_defensive import get_xg_defensive_multipliers
from backend.data.fetchers.key_player_absence import get_key_player_absence_multipliers
from backend.data.overrides.loader import get_player_overrides
from backend.data.fetchers.suspensions import get_suspension_elo_delta

# Default chance-quality prior (kept equal for both teams; currently informational only).
CHANCE_QUALITY = 1.3


async def assemble(m: Match, home: Team, away: Team, db: Session) -> dict:
    """Build TeamInputs, venue context, and every lambda modifier for `m`.

    Returns a dict with keys: home_input, away_input, venue_context, modifiers
    (the **kwargs for predict_group_match), and sp_mults (used only for why-factors).
    """
    home_form, away_form = await asyncio.gather(
        get_recent_form(home.code), get_recent_form(away.code)
    )

    venue_home_bonus, venue_away_bonus = get_venue_bonuses(home.code, away.code, m.venue or "")
    home_override, away_override = get_player_overrides(home.code, away.code)
    home_susp = get_suspension_elo_delta(m.id, home.code)
    away_susp = get_suspension_elo_delta(m.id, away.code)

    home_input = TeamInput(
        elo=(home.elo or 1500.0) + venue_home_bonus + home_override + home_susp,
        form=home_form, chance_quality=CHANCE_QUALITY, code=home.code,
    )
    away_input = TeamInput(
        elo=(away.elo or 1500.0) + venue_away_bonus + away_override + away_susp,
        form=away_form, chance_quality=CHANCE_QUALITY, code=away.code,
    )

    alt_bonus = altitude_lambda_bonus(m.venue or "")
    rest_mults = (1.0, 1.0)
    travel_mults = (1.0, 1.0)
    if m.kickoff:
        rest_mults = rest_days_multipliers(home.code, away.code, m.kickoff, db)
        travel_mults = travel_multipliers(home.code, away.code, m.venue or "", m.kickoff, db)
    dr_mults = dead_rubber_multipliers(home.code, away.code, m.matchday or 1, m.group or "", db)

    sq_mults = get_squad_quality_multipliers(home.code, away.code)
    inj_mults, h2h_mults, wx_mults, lineup_mults, xg_attack_mults = await asyncio.gather(
        get_injury_multipliers(home.code, away.code),
        get_h2h_multipliers(home.code, away.code),
        get_weather_multipliers(home.code, away.code, m.venue or "", m.kickoff),
        get_lineup_multipliers(home.code, away.code),
        get_squad_attack_multipliers(home.code, away.code),
    )
    sp_mults = get_set_piece_multipliers(home.code, away.code)
    # Real rolling xG from our harvested FixtureArchive (zero API cost, DB-only).
    # Neutral 1.0 below the min-sample floor so it adds nothing until a team has
    # enough archived fixtures — then it nudges attack lambda toward actual xG.
    xg_form_mults = get_xg_form_multipliers(home.code, away.code)
    # NEW (Phase 2, 2026-06-28): defensive xG from the same FixtureArchive
    # — pairs with xg_form_mults so the model finally has a real defence
    # signal beyond ELO. (home_def_mult, away_def_mult): keyed on the
    # OPPOSING team's xG-conceded — a strong-defending opponent damps your
    # attack lambda; a leaky one boosts it. Same ±5% cap, same min-sample
    # floor as the attack side so the pair composes neutrally pre-data.
    xg_def_mults = get_xg_defensive_multipliers(home.code, away.code)
    # Squad availability: sidelined key players + XI rotation, harvested. Also
    # DB-only + neutral until data exists. A depleted/rotated side scores less.
    avail_mults = get_squad_availability_multipliers(home.code, away.code)
    # NEW (Phase 3, 2026-06-28): player-weighted absence. squad_availability
    # treats every sidelined player the same; this weights by their share of
    # the team's recent (goals + assists) so losing your top scorer hits
    # materially harder than losing a third-choice keeper. Same DB-only,
    # neutral-pre-data contract; same ±5% cap.
    key_absence_mults = get_key_player_absence_multipliers(home.code, away.code)
    # Composite cap. Six lambda modifiers each capped at ±5% individually
    # would in the worst case compound to (1.05)^6 = 1.34 or (0.95)^6 = 0.74,
    # a ±30% swing on lambda. That's larger than the ELO+DC core wants —
    # ELO movement of 100 points is ~2.0× as much signal as the entire
    # modifier stack. Clamp the composite product to ±15% so correlated
    # modifiers (eg defensive xG + absences both bad for the same team)
    # can't gang up beyond what's justified by the modifier-level evidence.
    _COMPOSITE_LO, _COMPOSITE_HI = 0.85, 1.15

    def _clamp(x: float) -> float:
        return max(_COMPOSITE_LO, min(_COMPOSITE_HI, x))

    home_composite = (
        xg_attack_mults[0] * sp_mults[0] * xg_form_mults[0]
        * xg_def_mults[0] * avail_mults[0] * key_absence_mults[0]
    )
    away_composite = (
        xg_attack_mults[1] * sp_mults[1] * xg_form_mults[1]
        * xg_def_mults[1] * avail_mults[1] * key_absence_mults[1]
    )
    xg_mults = (round(_clamp(home_composite), 4), round(_clamp(away_composite), 4))

    return {
        "home_input": home_input,
        "away_input": away_input,
        "venue_context": {
            "home_bonus": venue_home_bonus,
            "away_bonus": venue_away_bonus,
            "venue": m.venue or "",
        },
        "modifiers": {
            "altitude_bonus": alt_bonus,
            "rest_multipliers": rest_mults,
            "dead_rubber_multipliers": dr_mults,
            "squad_quality_multipliers": sq_mults,
            "injury_multipliers": inj_mults,
            "h2h_multipliers": h2h_mults,
            "weather_multipliers": wx_mults,
            "travel_multipliers": travel_mults,
            "lineup_multipliers": lineup_mults,
            "xg_multipliers": xg_mults,
        },
        "sp_mults": sp_mults,
    }
